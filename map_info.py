def convert_room_id(current_room):
    if current_room == 1:
        return "Cafeteria"
    elif current_room == 2:
        return "MedBay"
    elif current_room == 3:
        return "Upper Engine"
    elif current_room == 4:
        return "Lower Engine"
    elif current_room == 5:
        return "Electrical"
    elif current_room == 6:
        return "Storage"
    elif current_room == 7:
        return "Communications"
    elif current_room == 8:
        return "Shields"
    elif current_room == 9:
        return "Weapons"
    elif current_room == 10:
        return "Navigation"
    elif current_room == 11:
        return "Reactor"
    elif current_room == 12:
        return "Security"
    elif current_room == 13:
        return "O2"
    elif current_room == 14:
        return "Admin"
